    private HumanoidCharacterProfile BuildRecordIdentityProfile(GeneralStationRecord stationRecord, CriminalRecord criminalRecord)
    {
        var effectiveSpecies = string.IsNullOrWhiteSpace(stationRecord.Species)
            ? (string) SharedHumanoidAppearanceSystem.DefaultSpecies
            : stationRecord.Species;
        var profile = criminalRecord.PortraitProfileSnapshot != null
            ? new HumanoidCharacterProfile(criminalRecord.PortraitProfileSnapshot)
            : HumanoidCharacterProfile.DefaultWithSpecies(effectiveSpecies);

        return profile
            .WithName(stationRecord.Name)
            .WithSpecies(effectiveSpecies)
            .WithAge(stationRecord.Age)
            .WithGender(stationRecord.Gender)
            .WithNationality(stationRecord.Nationality)
            .WithEmployer(stationRecord.Employer);
    }

    private void UpdateRecordIdentityProfile(HumanoidCharacterProfile profile, bool sendUpdate, GeneralStationRecord? rawRecord = null)
    {
        profile = NormalizeRecordIdentityProfile(profile);
        _recordProfile = profile;

        if (rawRecord != null)
        {
            _speciesUnset = IsIdentitySelectorBlank(rawRecord.Species);
            _nationalityUnset = IsIdentitySelectorBlank(rawRecord.Nationality);
            _employerUnset = IsIdentitySelectorBlank(rawRecord.Employer);
            _genderUnset = IsIdentitySelectorBlank(rawRecord.Species);
        }

        _suppressIdentityEditorEvents = true;
        try
        {
            PopulateSpeciesSelector(profile.Species, _speciesUnset);
            PopulateNationalitySelector(profile, _nationalityUnset);
            PopulateEmployerSelector(profile, _employerUnset);

            if (_genderUnset)
                ApplyOptionButtonPlaceholder(GenderButton, Loc.GetString("generic-not-available-shorthand"));
            else
                GenderButton.SelectId((int) profile.Gender);

            AgeEdit.Text = rawRecord != null && rawRecord.Age <= 0
                ? string.Empty
                : profile.Age.ToString();
        }
        finally
        {
            _suppressIdentityEditorEvents = false;
        }

        if (sendUpdate)
            SendIdentityUpdate(profile);
    }

    private void PopulateSpeciesSelector(string selectedSpecies, bool showPlaceholder)
    {
        SpeciesButton.Clear();

        for (var i = 0; i < _species.Count; i++)
        {
            SpeciesButton.AddItem(Loc.GetString(_species[i].Name), i);
            if (_species[i].ID == selectedSpecies)
                SpeciesButton.SelectId(i);
        }

        if (showPlaceholder)
